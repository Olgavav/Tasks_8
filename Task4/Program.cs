Console.Clear();

 
int[,,] array3D = new int[2, 2, 2];
CreateArray(array3D);
WriteArray(array3D);


void WriteArray (int[,,] array3D)
{
  for (int i = 0; i < array3D.GetLength(0); i++)
  {
    for (int j = 0; j < array3D.GetLength(1); j++)
    {
      for (int k = 0; k < array3D.GetLength(2); k++)
      {
        Console.Write($"{array3D[i, j, k]} ({i}, {j}, {k}) ");
      }
    Console.WriteLine();  
    }
  }
}

